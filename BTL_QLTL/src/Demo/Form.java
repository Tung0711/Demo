    public void loadData(){
        try{
            Connect a = new Connect();
            Connection conn = a.getConnection();
            int number;
            Vector row,column;
            column = new Vector();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select * from Nhanvien");
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount();
            
            for(int i = 1;i <= number; i++){
                column.add(metadata.getColumnName(i));
            }
            tbn.setColumnIdentifiers(column);
            
            while (rs.next()){
                row = new Vector();
                for(int i = 1;i <= number; i++){
                    row.addElement(rs.getString(i));
                }
                tbn.addRow(row);
                jTable.setModel(tbn);
            }
            
            jTable.getSelectionModel().addListSelectionListener(new ListSelectionListener(){

                @Override
                public void valueChanged(ListSelectionEvent e) {
                   if(jTable.getSelectedRow() >=0){
                       txt_MaNV.setText(jTable.getValueAt(jTable.getSelectedRow(), 0) + "");
                       txt_TenNV.setText(jTable.getValueAt(jTable.getSelectedRow(), 1) + "");
                       txt_namSinh.setText(jTable.getValueAt(jTable.getSelectedRow(), 2) + "");
                       txt_sdt.setText(jTable.getValueAt(jTable.getSelectedRow(), 3) + "");
                       txt_soNC.setText(jTable.getValueAt(jTable.getSelectedRow(), 4) + "");
                   }
                }
            });
        }catch(Exception ex){
            System.out.print(ex.toString());
    }
}
    
