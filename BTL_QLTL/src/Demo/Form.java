    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        try {
            Connect a = new Connect();
            Connection conn = a.getConnection();
            PreparedStatement pr = conn.prepareStatement("Delete Nhanvien where MaNV=?");
            pr.setString(1, jTable.getValueAt(jTable.getSelectedRow(), 0).toString());
            if(JOptionPane.showConfirmDialog(this,"Delete this Nhanvien?","Confirm",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                pr.executeUpdate();
                tbn.setRowCount(0);
                loadData();
            } 
        }catch (Exception ex){
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

